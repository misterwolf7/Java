
public class C_StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World"); // "": 문자열String
		System.out.println('H'); // '': 문자Character
		
		//new line
		System.out.println("Hello \nWorld"); // 줄바꿈
		System.out.println("Hello \"World\"");
		
		System.out.println("Hello World"); // len
		System.out.println("Hello, [[[name]]] ... bye.".replace("[[[name]]]","egoing"));//replace: 문자열 바꾸기
		
		
	}

}
