package defualtpakage;

public class A_FirstMethod {

	// main이라는 메소드를 사용한것
	// 자바는 FirstMethod를 실행해줘 -> main 이라는 메소드를 실행하도록 약속.
	public static void main(String[] args) {
		System.out.println("Hello Method");
		// println : "Hello Method"이라는 것을 출력해주는 메소드
		System.out.println(Math.floor(1.1));
		// Math라는 클래승 안의 floor이라는 함수 사용
	}

}
